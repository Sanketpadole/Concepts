package Program;

public class SwitchCase5 {

	public static void main(String[] args) {

		String mobile = "iPhone";
		switch (mobile) {
		case "samsung":
			System.out.println("Buy a Samsung phone");
			break;

		case "iPhone":
			System.out.println("Buy an iPhone");
			break;

		case "Motorola":
			System.out.println("Buy a Motorola phone");

		}

	}

}
